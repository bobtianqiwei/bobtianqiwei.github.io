function renderPage(page) {
  if (page.layout === "publication") return renderPublicationPage(page);
  if (page.layout === "legacy-hero") return renderLegacyHeroPage(page);
  if (page.layout === "album") return renderAlbumPage(page);
  throw new Error(`Unsupported page layout: ${page.layout}`);
}

