function renderImage(imgSrc, extraClass = "image-100") {
  return `<img src="${imgSrc}" loading="lazy" alt="" class="${extraClass}">`;
}

function renderAnchorCard(item, titleClass = "work-page-project-name", imageClass = "image-38") {
  const target = isExternal(item.href) ? ' target="_blank"' : "";
  const script = item.lightbox
    ? `\n            <script type="application/json" class="w-json">${JSON.stringify(item.lightbox, null, 2)}</script>`
    : "";
  const href = item.lightbox ? "#" : item.href;
  const className = item.lightbox ? "lightbox-link w-inline-block w-lightbox" : item.className;
  return `        <div class="w-container">
          <a href="${href}"${target} class="${className}">${renderImage(item.image, imageClass)}
            <div class="${titleClass}">${item.contentHtml}</div>${script}
          </a>
        </div>`;
}

function renderThreeColumnSection(items, heading) {
  const columns = [[], [], []];
  items.forEach((item, index) => {
    columns[index % 3].push(renderAnchorCard(item));
  });
  return `    <h1 class="big-title-3">${heading}</h1>
    <div class="work-columns w-row">
      <div class="column-13 w-col w-col-4">
${columns[0].join("\n")}
      </div>
      <div class="column-14 w-col w-col-4">
${columns[1].join("\n")}
      </div>
      <div class="column-15 w-col w-col-4">
${columns[2].join("\n")}
      </div>
    </div>`;
}

